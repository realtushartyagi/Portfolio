type Direction = "left" | "right" | "up" | "down";
type TransitionType = "spring" | "tween" | "inertia" | string;

interface SlideInVariants {
  hidden: {
    x: string | number;
    y: string | number;
  };
  show: {
    x: number;
    y: number;
    transition: {
      type: TransitionType;
      delay: number;
      duration: number;
      ease: string;
    };
  };
}

const slideIn = (
  direction: Direction,
  type: TransitionType,
  delay: number,
  duration: number
): SlideInVariants => {
  return {
    hidden: {
      x: direction === "left" ? "-100%" : direction === "right" ? "100%" : 0,
      y: direction === "up" ? "100%" : direction === "down" ? "100%" : 0,
    },
    show: {
      x: 0,
      y: 0,
      transition: {
        type: type,
        delay: delay,
        duration: duration,
        ease: "easeOut",
      },
    },
  };
};

